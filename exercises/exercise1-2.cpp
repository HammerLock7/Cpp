#include <iostream>

int main(){
    std::cout<< "***************************Blueberry Muffin***************************"<<'\n';
    std::cout<< "Ingredients"<<'\n';
    std::cout<<'\t'<<"1. 6-8 ounces Blueberries"<<'\n';
    std::cout<<'\t'<<"2. 195g Flour"<<'\n';
    std::cout<<'\t'<<"3. 150g Sugar"<<'\n';
    std::cout<<'\t'<<"4. 80ml Oil"<<'\n';
    std::cout<<'\t'<<"5. 80ml Milk"<<'\n';
    std::cout<<'\t'<<"6. 1 large Egg"<<'\n';
    std::cout<<'\t'<<"7. 2tsp Baking Powder"<<'\n';
    std::cout<<'\t'<<"8. 1 1/2tsp vanilla extract "<<'\n';
    std::cout<<'\t'<<"9. 1/4tsp sea salt"<<'\n';

    std::cout<<"Directions"<<'\n';
    std::cout<<'\t'<<"1. Preheat oven to 204C, and grease the muffin tin with oil to prevent sticking. "<<'\n';
    std::cout<<'\t'<<"2. Make batter: mix the flour, sugar, baking powder, and salt in a large bowl"<<'\n';
    std::cout<<'\t'<<"3. mix egg, oil, milk, and vanilla extract in a cup."<<'\n';
    std::cout<<'\t'<<"4. Add the wet ingredients with the dry ingredients in the bowl and stir gently."<<"\n";
    std::cout<<'\t'<<"5. Gently fold in the blueberries."<<'\n';
    std::cout<<'\t'<<"6. Bake muffins: divide the batter between muffin cups"<<'\n';
    std::cout<<'\t'<<"7. Bake muffins for 15 to 20 minutes or until the top is no longer wet and a toothpick inserted into the middle of a muffin comes out with crumbs, not wet batter"<<'\n';
    std::cout<<"***************************The End**********************************"<<'\n';

    return 0;
}